def require_auth(f):
    @wraps(f)
    def decorated_function(*args, **kwargs):
        api_key = request.headers.get('X-API-Key')
        if not api_key or api_key not in users:
            return jsonify({'error': 'Invalid API key'}), 401
        request.user = users[api_key]
        return f(*args, **kwargs)
    return decorated_function

def can_generate(user):
    """Check if user can generate a document based on their plan"""
    plan = user['plan']
    current_month = datetime.now().strftime('%Y-%m')
    
    if plan == 'free':
        # Track total free files generated
        if user.get('free_files_used', 0) >= PLANS['free']['max_free_files']:
            return False, "Free limit reached. Please upgrade to continue."
        return True, None
    
    elif plan == 'subscription':
        # Check if subscription is active
        if user.get('subscription_active', False):
            # Check monthly limit? (Optional: add fair usage limit)
            if current_month not in user['usage']:
                user['usage'][current_month] = 0
            if user['usage'][current_month] >= 100:  # 100 files/month fair usage
                return False, "Monthly fair usage limit reached. Contact support if you need more."
            return True, None
        else:
            return False, "Subscription expired. Please renew."
    
    elif plan == 'pay_as_you_go':
        # Check credits
        if user_credits.get(user['email'], 0) >= 0.10:  # At least $0.10 credit
            return True, None
        else:
            return False, "Insufficient credits. Please purchase more credits."
    
    return False, "Invalid plan"

def deduct_credits(user):
    """Deduct credits for pay-as-you-go users"""
    if user['plan'] == 'pay_as_you_go':
        current_credits = user_credits.get(user['email'], 0)
        user_credits[user['email']] = current_credits - 0.10
        
    elif user['plan'] == 'free':
        user['free_files_used'] = user.get('free_files_used', 0) + 1
        
    elif user['plan'] == 'subscription':
        current_month = datetime.now().strftime('%Y-%m')
        user['usage'][current_month] = user['usage'].get(current_month, 0) + 1

def add_watermark_to_pdf(pdf_buffer, watermark_text="Generated by ABSON CV Generator"):
    """Add watermark to PDF"""
    # Create a new PDF with watermark
    packet = BytesIO()
    can = canvas.Canvas(packet, pagesize=letter)
    width, height = letter
    
    # Add watermark text diagonally
    can.saveState()
    can.setFont('Helvetica', 20)
    can.setFillColorRGB(0.7, 0.7, 0.7, alpha=0.3)
    can.translate(width/2, height/2)
    can.rotate(45)
    can.drawCentredString(0, 0, watermark_text)
    can.restoreState()
    can.save()
    
    # Merge watermarks
    packet.seek(0)
    return packet

