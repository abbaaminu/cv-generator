@app.route('/api/webhook/stripe', methods=['POST'])
def stripe_webhook():
    payload = request.get_data(as_text=True)
    sig_header = request.headers.get('Stripe-Signature')
    
    try:
        event = stripe.Webhook.construct_event(
            payload, sig_header, os.getenv('STRIPE_WEBHOOK_SECRET')
        )
        
        if event['type'] == 'checkout.session.completed':
            session = event['data']['object']
            customer_email = session['client_reference_id']
            
            # Update user based on purchase type
            if 'subscription' in session.get('metadata', {}).get('plan', ''):
                # Update to subscription plan
                for api_key, user in users.items():
                    if user['email'] == customer_email:
                        user['plan'] = 'subscription'
                        user['subscription_active'] = True
                        break
            else:
                # Add credits
                credit_amount = float(session['metadata'].get('credit_amount', 0))
                user_credits[customer_email] = user_credits.get(customer_email, 0) + credit_amount
                
                # Update plan if they have credits and were free
                for api_key, user in users.items():
                    if user['email'] == customer_email and user['plan'] == 'free':
                        user['plan'] = 'pay_as_you_go'
                        break
        
        elif event['type'] == 'customer.subscription.deleted':
            # Handle subscription cancellation
            subscription = event['data']['object']
            customer_email = subscription['metadata'].get('email')
            if customer_email:
                for api_key, user in users.items():
                    if user['email'] == customer_email:
                        user['subscription_active'] = False
                        user['plan'] = 'pay_as_you_go' if user_credits.get(customer_email, 0) > 0 else 'free'
                        break
        
        return jsonify({'status': 'success'})
    except Exception as e:
        return jsonify({'error': str(e)}), 400

