@app.route('/api/export-pdf/<doc_id>', methods=['GET'])
@require_auth
def export_pdf(doc_id):
    if doc_id not in generations:
        return jsonify({'error': 'Document not found'}), 404
    
    doc = generations[doc_id]
    
    # Create simple PDF with content
    buffer = BytesIO()
    c = canvas.Canvas(buffer, pagesize=letter)
    width, height = letter
    
    # Add content
    y = height - 50
    c.setFont("Helvetica", 12)
    
    for line in doc['content'].split('\n'):
        if y < 50:
            c.showPage()
            y = height - 50
            c.setFont("Helvetica", 12)
        
        # Handle long lines
        if len(line) > 80:
            words = line.split()
            current_line = ""
            for word in words:
                if len(current_line + word) < 80:
                    current_line += word + " "
                else:
                    c.drawString(50, y, current_line)
                    y -= 15
                    current_line = word + " "
            if current_line:
                c.drawString(50, y, current_line)
                y -= 15
        else:
            c.drawString(50, y, line)
            y -= 15
    
    # Add watermark if needed
    if doc['has_watermark']:
        c.saveState()
        c.setFont("Helvetica", 16)
        c.setFillColorRGB(0.5, 0.5, 0.5, alpha=0.3)
        c.translate(width/2, height/2)
        c.rotate(45)
        c.drawCentredString(0, 0, "Generated by ABSON CV Generator")
        c.restoreState()
    
    c.save()
    buffer.seek(0)
    
    return send_file(
        buffer,
        as_attachment=True,
        download_name=f"{doc['type']}_{doc_id}.pdf",
        mimetype='application/pdf'
    )

